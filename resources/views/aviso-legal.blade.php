                    <li><strong>Errores en origen:</strong> las administraciones públicas pueden publicar datos con erratas o información incompleta.</li>
                    <li><strong>Errores de procesamiento:</strong> el proceso automático de extracción, normalización y carga de datos puede introducir errores adicionales.</li>
